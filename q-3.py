# Create Tuples
t = ( 1, 2, "Computer", -2.5, -1, 3.5)
print (t)
print (type(t))

# Convert tuple into list
t_list = list(t)
print (t_list)
print (type(t_list))

# Delete tuples elements
t_list.remove(1)
print (t_list)

# Convert list into tuple
t_new = tuple(t_list)
print (t_new)
print (type(t_new))

